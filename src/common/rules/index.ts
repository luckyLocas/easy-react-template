/**
 * 常规字符
 * 特殊字符“空格、换行和英文格式的`^*;'\"|, /<>?”除外
 */
export const regularText = {
    pattern: /^[^`\s\^\*;'"\\|,/<>\?]*$/,
    message: '请不要输入空格、换行和`^*;\'\\\"|, /<>?'
}

/**
 * 组织机构代码
 * 8位数字或大写字母”+“-”+“1位数字或大写字母
 */
export const orgCode = {
    pattern: /^[`\^\*;'"\|,/<>\?]/,
    message: '请不要输入空格、换行和`^*;\'\\\"|, /<>?'
}

/**
 * 数字
 * 8位数字或大写字母”+“-”+“1位数字或大写字母
 */
export const onlyNumber = {
    pattern: /^\d+$/,
    message: '请输入数字'
}